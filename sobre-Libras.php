        <div class="info-section">
            <h2><i class="bi bi-info-circle-fill text-success me-2"></i>O que é Libras?</h2>
            <p>
                A Libras (Língua Brasileira de Sinais) é a língua de sinais utilizada pelas comunidades surdas do Brasil. 
                É uma língua natural, com estrutura gramatical própria, que não é simplesmente uma versão sinalizada do 
                português, mas sim um sistema linguístico completo e independente.
            </p>
            <p>
                Reconhecida oficialmente pela Lei nº 10.436, de 24 de abril de 2002, a Libras é considerada o meio legal 
                de comunicação e expressão das comunidades surdas brasileiras.
            </p>
        </div>

        <div class="info-section">
            <h2><i class="bi bi-clock-history text-success me-2"></i>História da Libras</h2>
            
            <div class="timeline-item">
                <div class="timeline-dot"></div>
                <div class="timeline-content">
                    <h5 class="fw-bold">1857 - Início no Brasil</h5>
                    <p>
                        O educador francês Ernest Huet, que era surdo, chegou ao Brasil a convite de D. Pedro II e 
                        fundou a primeira escola para surdos do país, o Imperial Instituto de Surdos Mudos (atual INES).
                    </p>
                </div>
            </div>

            <div class="timeline-item">
                <div class="timeline-dot"></div>
                <div class="timeline-content">
                    <h5 class="fw-bold">1880 - Congresso de Milão</h5>
                    <p>
                        Um marco negativo: o Congresso Internacional de Educação de Surdos proibiu o uso de línguas de 
                        sinais na educação, priorizando o oralismo. Isso gerou quase 100 anos de repressão às línguas de sinais.
                    </p>
                </div>
            </div>

            <div class="timeline-item">
                <div class="timeline-dot"></div>
                <div class="timeline-content">
                    <h5 class="fw-bold">1960-1980 - Reconhecimento Científico</h5>
                    <p>
                        Pesquisas linguísticas comprovaram que as línguas de sinais são línguas naturais e completas, 
                        com toda a complexidade das línguas orais.
                    </p>
                </div>
            </div>

            <div class="timeline-item">
                <div class="timeline-dot"></div>
                <div class="timeline-content">
                    <h5 class="fw-bold">2002 - Reconhecimento Legal</h5>
                    <p>
                        A Lei nº 10.436 reconheceu oficialmente a Libras como meio legal de comunicação e expressão 
                        das comunidades surdas brasileiras.
                    </p>
                </div>
            </div>

            <div class="timeline-item">
                <div class="timeline-dot"></div>
                <div class="timeline-content">
                    <h5 class="fw-bold">2005 - Regulamentação</h5>
                    <p>
                        O Decreto nº 5.626 regulamentou a Lei de Libras, estabelecendo diretrizes para seu ensino, 
                        formação de profissionais e inclusão da disciplina de Libras nos cursos de formação de professores.
                    </p>
                </div>
            </div>
