                                <h6 class="text-muted mb-3">Lei de Reconhecimento da Libras</h6>
                                <p class="card-text">
                                    Esta é a lei mais importante para a comunidade surda brasileira. Ela reconhece oficialmente
                                    a Língua Brasileira de Sinais (Libras) como meio legal de comunicação e expressão.
                                </p>
                                <div class="bg-light p-3 rounded mt-3">
                                    <h6 class="fw-bold">Principais Pontos:</h6>
                                    <ul class="mb-0">
                                        <li>Reconhece Libras como língua de comunicação das comunidades surdas</li>
                                        <li>Estabelece que Libras não pode substituir a modalidade escrita da língua portuguesa</li>
                                        <li>Determina apoio governamental ao uso e difusão da Libras</li>
                                        <li>Garante atendimento e tratamento adequado aos surdos nos serviços públicos</li>
                                    </ul>
                                </div>
